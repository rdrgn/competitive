using I = vector<int>;
using O = int;

O solve(const I& input) {
  vector<int> a, b;
  for (int i = 0; input[i]; i++) a.push_back(input[i]);
  for (int i = a.size() + 1; input[i]; i++) b.push_back(input[i]);

  int r = 0;
  for (int o = -200; o <= 200; o++) {
    int ro = 0;
    for (int i = 0; i < a.size(); i++) {
      for (int j = 0; j < b.size(); j++) {
        if (a[i] + o == b[j]) ro++;
      }
    }
    r = max(r, ro);
  }
  return r;
}

